#include <stdio.h>

/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: addition of two integers result
 */

int add(int a, int b)
{
	return(a + b);
}

/**
 * mod - modulus two integers
 * @a: first integer
 * @b: second integer
 * Return: modulus final result
 */

int  mod(int a, int b)
{
	return(a % b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: Subtracted final result
 */

int sub(int a, int b)
{
	return(a - b);
}

/**
 * div - division of two integers
 * @a: first integer
 * @b: second integer
 * Return: division final result
 */

int  div(int a, int b)
{
        return(a / b);
}

/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: multiplication final result
 */

int  mul(int a, int b)
{
	return(a * b);
}
